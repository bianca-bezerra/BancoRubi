CREATE OR REPLACE FUNCTION DELETO(NOME_CLIENTE TEXT) RETURNS VOID AS $$
    DECLARE ID_CLIENTE_ INT;
BEGIN
    SELECT GET_ID_CONTA(NOME_CLIENTE) INTO ID_CLIENTE_;
